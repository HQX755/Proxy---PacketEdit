/*
*	Fast iteration. 
*/
static const char * GetHeaderByKey(unsigned short key)
{
	static unsigned int size = sizeof(Opcodes) / sizeof(*Opcodes);
	static unsigned int middle = size / 2;
	static unsigned int outofrange = middle;

	static unsigned short k = Opcodes[middle].key;
	static unsigned short kMax = Opcodes[size - 1].key;

	if (key < k)
	{
		while (key < k)
		{
			outofrange = middle;
			middle /= 2;
			k = Opcodes[middle].key;
		}

		for (unsigned i = middle; i <= outofrange; ++i)
		{
			if (Opcodes[i].key == key)
			{
				return Opcodes[i].code;
			}
		}
	}
	else
	{
		if (key > kMax)
		{
			return "UNKNOWN_OPCODE";
		}

		while (key > k)
		{
			outofrange = middle;
			middle += (size - middle) / 2;
			k = Opcodes[middle].key;
		}

		for (unsigned i = outofrange; i <= middle; ++i)
		{
			if (Opcodes[i].key == key)
			{
				return Opcodes[i].code;
			}
		}
	}

	return "UNKNOWN_OPCODE";
}

